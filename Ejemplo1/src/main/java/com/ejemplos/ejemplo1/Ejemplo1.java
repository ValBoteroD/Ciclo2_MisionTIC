package com.ejemplos.ejemplo1;

//las clase es la caracteristica general o global de los objetos por crear
public class Ejemplo1 {
/*
    public: es el acceso del metodo, tambien esta el acceso private, protected
    metodo: es una accion que se ejecuta en tiempo real 
    static: describe el comportamiento del metodo 
    void: vacio-- no retorna ningun tipo de dato, o retorna vacio
    main: es el metodo principal 
    (string[] args) son los parametros de dicho metodo
    */
    public static void main(String[] args) {
        // la forma de mostrar datos por consola
        System.out.println("Hello World!");
        System.out.println("Hola tripulantes");
    }
}
